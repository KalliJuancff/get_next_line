#include "get_next_line.h"

char	*get_next_line(int fd)
{
	static char	*buffer;

	buffer = ft_calloc (BUFFER_SIZE + 1, sizeof(char));
	while ((read (fd, buffer, BUFFER_SIZE)) == BUFFER_SIZE)
	{
	}
	if (*buffer == '\0')
		return (NULL);
	return (buffer);
}
